namespace Model;

/**
 * Class Contact
 *
 */
class Contact
{
    private $id;
    private $title;
    private $content;


    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }

    /**
     * @param int $id
     *
     * @return Contact
     */
    public function setId(int $id): Contact
    {
        $this->id = $id;

        return $this;
    }

    /**
     * @return string
     */
    public function getTitle(): string
    {
        return $this->title;
    }

    /**
     * @param string $title
     *
     * @return Contact
     */
    public function setTitle(string $title): Contact
    {
        $this->title = $title;

        return $this;
    }

    /**
     * @return string
     */
    public function getContent(): string
    {
        return $this->content;
    }

    /**
     * @param string $content
     *
     * @return Contact
     */
    public function setContent(string $content): Contact
    {
        $this->content = $content;

        return $this;
    }
}
